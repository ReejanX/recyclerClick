package com.example.recyclerhero.model;

public class Person {

    public String name = "";
    public String address = "";
    public String phoneNumber = "";
    public String citizenship = "";

    public Person(String name, String address, String phoneNumber, String citizenship) {
        this.name = name;
        this.address = address;
        this.phoneNumber = phoneNumber;
        this.citizenship = citizenship;
    }
}
